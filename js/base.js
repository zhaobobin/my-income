//获取url中"?"符后的字串
function GetRequest() {
  var url = location.search;
  var theRequest = new Object();
  if (url.indexOf('?') != -1) {
    var str = url.substring(1);
    var strs = str.split('&');
    for (var i = 0; i < strs.length; i++) {
      theRequest[strs[i].split('=')[0]] = unescape(strs[i].split('=')[1]);
    }
  }
  return theRequest;
}

// 获取当前月
function getCurrentMonth() {
  var date = new Date();
  var year = date.getFullYear().toString();
  var month = (date.getMonth() + 1).toString();
  if (month.length === 1) {
    month = '0' + month;
  }
  return year + month;
}

// 生成年月选项
function getYearList(n) {
  var year = (new Date().getFullYear() - n).toString();
  return { name: year, value: year };
}

// 生成日期选项
function getAgoDay(n) {
  var date = new Date();
  var newDate = new Date(date.getTime() - n * 24 * 60 * 60 * 1000);
  var year = newDate.getFullYear().toString();
  var month = (newDate.getMonth() + 1).toString();
  var day = newDate.getDate().toString();
  // var en_mon_arr = ["Jan", "Feb", "Mar", "Apr", "May", "Jun", "Jul", "Aug", "Spt", "Oct", "Nov", "Dec"];
  var monthValue = month;
  var dayValue = day;
  if (monthValue.length === 1) {
    monthValue = '0' + monthValue;
  }
  if (dayValue.length === 1) {
    dayValue = '0' + dayValue;
  }
  return {
    name: month + '-' + day,
    value: year + monthValue + dayValue,
  };
}

function getAllDays(n) {
  var dayList = [];
  for (var i = n; i > 0; i--) {
    dayList.push(getAgoDay(i));
  }
  return dayList;
}

function getAllYears(n) {
  var date = new Date();
  var yearList = [];
  var months = [];
  var currentYear = date.getFullYear().toString();
  var currentMonth = date.getMonth() + 1;
  monthList.forEach(function(month) {
    if (month.id <= currentMonth) {
      months.push(month);
    }
  });
  for (var i = n; i >= 0; i--) {
    var yaer = getYearList(i);
    yaer.child = yaer.value === currentYear ? months : monthList;
    yearList.push(yaer);
  }
  return yearList;
}

